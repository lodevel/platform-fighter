/**
 * Stable identifier for a stored replay. Generated by
 * {@link ReplayStorageOptions.idFactory} (default: `crypto.randomUUID`),
 * unique within a backend. The replay menu uses this as React/DOM
 * row keys; the (later-AC) share flow embeds it in URLs.
 */
export type ReplayStorageId = string;

/**
 * Lightweight summary returned by {@link ReplayStorage.list} so the
 * replay browser can render every row without loading every replay's
 * full input timeline.
 *
 * The fields are split across two sources:
 *
 *   • Backend-managed: `id`, `savedAt`, `sizeBytes` — derived at save
 *     time, not stored inside the {@link ReplayFile} itself.
 *   • Mirrored from the replay header so the UI can show stage / players
 *     / duration without hitting `load()` for every row.
 */
export interface StoredReplayMetadata {
  /** Unique id assigned at save time. */
  readonly id: ReplayStorageId;
  /** ISO 8601 timestamp the replay was *saved* (not recorded). */
  readonly savedAt: string;
  /** UTF-8 byte size of the serialised replay JSON. */
  readonly sizeBytes: number;
  /** Mirrored from `replay.metadata.recordedAt`. */
  readonly recordedAt: string;
  /** Mirrored from `replay.metadata.durationFrames`. */
  readonly durationFrames: number;
  /** Mirrored from `replay.metadata.notes` (truncated by serialiser to 1024). */
  readonly notes: string;
  /** Mirrored from `replay.matchConfig.stageId`. */
  readonly stageId: string;
  /** Mirrored from `replay.metadata.playerCount`. */
  readonly playerCount: number;
  /** Mirrored from `replay.rngSeed`. */
  readonly rngSeed: number;
  /** Engine `package.json#version` recorded at save time. */
  readonly engineVersion: string;
  /**
   * Integrity checksum of the serialised payload — AC 30104 Sub-AC 4.
   *
   * Computed once at save time from the canonical compact JSON
   * `serializeReplayForStorage` produces. The storage layer re-computes
   * the checksum on every load and throws
   * {@link ReplayIntegrityError} (re-wrapped as
   * {@link ReplayStorageError}) when the two disagree, so a corrupted
   * row never silently feeds bad bytes into the replay player.
   *
   * 16 lowercase hex characters (64 bits). Pinned to the algorithm
   * named by {@link checksumAlgorithm}; future algorithm bumps add a
   * new identifier rather than mutating this one in place.
   */
  readonly checksum: string;
  /**
   * Identifier of the algorithm used to produce {@link checksum}.
   * Carried alongside the checksum so a future migration can introduce
   * a stronger hash without invalidating old rows: the verifier
   * branches on this string to choose the matching implementation.
   *
   * Today: always `'fnv1a-64-v1'` — the only algorithm this build
   * understands. See {@link ./replayChecksum.CHECKSUM_ALGORITHM}.
   */
  readonly checksumAlgorithm: ReplayChecksumAlgorithm;
}

/**
 * Full stored replay record returned by {@link ReplayStorage.load}.
 * Wraps the {@link ReplayFile} with the storage-side metadata so the
 * caller doesn't have to make two trips for "load + show in list".
 */
export interface StoredReplay {
  /** Storage-side metadata (id, savedAt, sizeBytes, …). */
  readonly metadata: StoredReplayMetadata;
  /** The frozen, validated replay file payload. */
  readonly replay: ReplayFile;
}

/** Aggregate stats for {@link ReplayStorage.getStats}. */
export interface ReplayStorageStats {
  /** Number of replays currently stored. */
  readonly count: number;
  /** Sum of every replay's `sizeBytes`. */
  readonly totalBytes: number;
  /**
   * Browser-reported total quota in bytes when known
   * (`navigator.storage.estimate().quota`), otherwise `null`. Always
   * `null` for the localStorage backend — browsers don't expose
   * localStorage's per-origin cap programmatically.
   */
  readonly quotaBytes: number | null;
  /**
   * Browser-reported usage in bytes when known. For IDB this is the
   * `navigator.storage.estimate().usage` figure (which counts everything
   * the origin stores, not just replays). For localStorage and memory
   * this is the same as `totalBytes`.
   */
  readonly usageBytes: number | null;
  /** Backend kind that produced these stats — useful for HUD diagnostics. */
  readonly backend: ReplayStorageBackend;
}

/** Backend kind discriminator. */
export type ReplayStorageBackend = 'indexeddb' | 'localstorage' | 'memory';

/** Options for {@link ReplayStorage.save}. */
export interface SaveReplayOptions {
  /**
   * Pre-allocated id. Optional — when omitted the storage generates one
   * via the configured `idFactory`. The (later-AC) "rename replay" flow
   * uses this to overwrite an existing row in place.
   */
  readonly id?: ReplayStorageId;
  /**
   * Wall-clock instant to stamp into `metadata.savedAt`. Defaults to a
   * `now()` call on the configured time source. Passing an explicit
   * value keeps tests stable.
   */
  readonly savedAt?: Date;
  /**
   * If true and a row with the given id already exists, overwrite it.
   * Otherwise, throw {@link ReplayStorageError}. Defaults to `false`
   * to prevent accidentally clobbering a previous match.
   */
  readonly overwrite?: boolean;
}

/** Shared constructor options across every backend. */
export interface ReplayStorageOptions {
  /**
   * Factory invoked once per `save()` to produce a row id. Defaults to
   * `crypto.randomUUID()` when available, otherwise a monotonic counter
   * suffixed with the wall clock. Tests inject a deterministic stub so
   * assertions can check exact ids.
   */
  readonly idFactory?: () => ReplayStorageId;
  /**
   * Wall-clock factory invoked at `save()` time to stamp
   * `metadata.savedAt`. Defaults to `() => new Date()`.
   */
  readonly nowFactory?: () => Date;
}

