/**
 * Options for {@link openReplayStorage}. Same shape as
 * {@link ReplayStorageOptions} plus a `prefer` knob the test suite uses
 * to force a specific backend.
 */
export interface OpenReplayStorageOptions extends ReplayStorageOptions {
  /**
   * Pin a specific backend. Default `'auto'` probes IDB → localStorage
   * → memory in order. Tests use the explicit values.
   */
  readonly prefer?: 'auto' | ReplayStorageBackend;
  /**
   * Optional database / prefix name. Defaults to `'platform-fighter-replays'`
   * (IDB database name) or `'pf:replay:'` (localStorage key prefix).
   * Exposed so tests can isolate runs.
   */
  readonly namespace?: string;
  /**
   * Optional injected IDBFactory. Defaults to `globalThis.indexedDB`.
   * Tests pass an in-memory shim.
   */
  readonly indexedDBFactory?: IDBFactory;
  /**
   * Optional injected Storage (localStorage). Defaults to
   * `globalThis.localStorage`. Tests pass an in-memory shim.
   */
  readonly localStorageRef?: Storage;
  /**
   * Optional `navigator.storage.estimate` override for tests. Returns
   * `null`/missing fields when unsupported. Production reads
   * `navigator.storage` directly.
   */
  readonly estimateStorage?: () => Promise<{ usage?: number; quota?: number }>;
}

/**
 * Probe the runtime and return the best available backend. Order:
 *
 *   1. IndexedDB if `globalThis.indexedDB` is callable AND
 *      `prefer !== 'localstorage' | 'memory'`.
 *   2. localStorage if `globalThis.localStorage` exposes get/set/remove
 *      AND IDB rejected (or `prefer === 'localstorage'`).
 *   3. {@link MemoryReplayStorage} as a last resort.
 *
 * The factory is the only async entry — IDB requires an open call. The
 * fallback backends are constructable synchronously, but the function
 * resolves uniformly so callers always `await`.
 */
export async function openReplayStorage(
  options: OpenReplayStorageOptions = {},
): Promise<ReplayStorage> {
  const prefer = options.prefer ?? 'auto';

  // Memory short-circuit — useful for tests + the headless CLI.
  if (prefer === 'memory') {
    return new MemoryReplayStorage(options);
  }

  if (prefer === 'auto' || prefer === 'indexeddb') {
    const idbFactory =
      options.indexedDBFactory ?? (globalThis as { indexedDB?: IDBFactory }).indexedDB;
    if (idbFactory !== undefined && typeof idbFactory.open === 'function') {
      try {
        // Lazy-import the IDB backend so headless callers don't pay the
        // module's parse cost when they only want memory storage.
        const mod = await import('./IndexedDBReplayStorage');
        return await mod.IndexedDBReplayStorage.open({
          ...options,
          indexedDBFactory: idbFactory,
        });
      } catch (err) {
        if (prefer === 'indexeddb') {
          // Caller explicitly wanted IDB — surface the failure rather
          // than silently falling back.
          throw new ReplayStorageError(
            `openReplayStorage: IndexedDB backend unavailable — ${
              err instanceof Error ? err.message : String(err)
            }`,
          );
        }
        // Auto mode — fall through to localStorage probe.
      }
    } else if (prefer === 'indexeddb') {
      throw new ReplayStorageError(
        'openReplayStorage: prefer=indexeddb but globalThis.indexedDB is unavailable',
      );
    }
  }

  if (prefer === 'auto' || prefer === 'localstorage') {
    const lsRef =
      options.localStorageRef ?? (globalThis as { localStorage?: Storage }).localStorage;
    if (lsRef !== undefined && typeof lsRef.setItem === 'function') {
      const mod = await import('./LocalStorageReplayStorage');
      return new mod.LocalStorageReplayStorage({
        ...options,
        localStorageRef: lsRef,
      });
    } else if (prefer === 'localstorage') {
      throw new ReplayStorageError(
        'openReplayStorage: prefer=localstorage but globalThis.localStorage is unavailable',
      );
    }
  }

  // Last resort.
  return new MemoryReplayStorage(options);
}

