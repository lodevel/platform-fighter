/**
 * In-process, non-persistent storage. Useful for:
 *
 *   • The vitest suite — no IDB shim needed.
 *   • Headless CLI tooling (e.g. an "export every replay to JSON" script).
 *   • A first-run demo mode where we don't want to clutter the user's
 *     `localStorage` until they save their first match.
 *
 * Implements the same interface as the persistent backends so feature
 * tests can swap in this backend with a single line.
 */
export class MemoryReplayStorage implements ReplayStorage {
  public readonly backend = 'memory' as const;
  private readonly idFactory: () => ReplayStorageId;
  private readonly nowFactory: () => Date;
  private readonly rows: Map<ReplayStorageId, { metadata: StoredReplayMetadata; serialized: string }> = new Map();
  private closed = false;

  constructor(options: ReplayStorageOptions = {}) {
    this.idFactory = options.idFactory ?? defaultIdFactory();
    this.nowFactory = options.nowFactory ?? (() => new Date());
  }

  async save(
    replay: ReplayFile,
    options: SaveReplayOptions = {},
  ): Promise<StoredReplayMetadata> {
    this.assertOpen();
    const validated = validateReplayForWrite(replay);
    const id = options.id ?? this.idFactory();
    if (typeof id !== 'string' || id.length === 0) {
      throw new ReplayStorageError('ReplayStorage.save: id must be a non-empty string');
    }
    if (this.rows.has(id) && options.overwrite !== true) {
      throw new ReplayStorageError(
        `ReplayStorage.save: row '${id}' already exists; pass overwrite: true to replace`,
      );
    }
    const serialized = serializeReplayForStorage(validated);
    const sizeBytes = utf8ByteLength(serialized);
    const checksum = computeReplayPayloadChecksum(serialized);
    const savedAt = (options.savedAt ?? this.nowFactory()).toISOString();
    const metadata = buildStoredReplayMetadata(
      id,
      savedAt,
      sizeBytes,
      validated,
      checksum,
    );
    this.rows.set(id, { metadata, serialized });
    return metadata;
  }

  async load(id: ReplayStorageId): Promise<StoredReplay | null> {
    this.assertOpen();
    const row = this.rows.get(id);
    if (row === undefined) return null;
    // AC 30104 Sub-AC 4 — verify the integrity checksum *before* the
    // JSON parse so a known-corrupted payload surfaces as a
    // ReplayCorruptedError rather than the parser's lower-level
    // "JSON is malformed" message. The verifier throws
    // ReplayCorruptedError on mismatch, which the storage interface
    // commits to surfacing untouched.
    assertReplayPayloadIntegrity(
      id,
      row.serialized,
      row.metadata.checksum,
      row.metadata.checksumAlgorithm,
    );
    let replay: ReplayFile;
    try {
      replay = deserializeReplay(JSON.parse(row.serialized));
    } catch (err) {
      const msg = err instanceof Error ? err.message : String(err);
      throw new ReplayStorageError(`ReplayStorage.load: row '${id}' is corrupt — ${msg}`);
    }
    return Object.freeze({ metadata: row.metadata, replay });
  }

  async loadOrThrow(id: ReplayStorageId): Promise<StoredReplay> {
    const r = await this.load(id);
    if (r === null) throw new ReplayNotFoundError(id);
    return r;
  }

  async list(): Promise<ReadonlyArray<StoredReplayMetadata>> {
    this.assertOpen();
    const all = [...this.rows.values()].map((r) => r.metadata);
    // Newest savedAt first; tie-break on id (lexicographic) for stable order.
    all.sort((a, b) => {
      if (a.savedAt !== b.savedAt) return a.savedAt < b.savedAt ? 1 : -1;
      return a.id < b.id ? -1 : a.id > b.id ? 1 : 0;
    });
    return Object.freeze(all);
  }

  async has(id: ReplayStorageId): Promise<boolean> {
    this.assertOpen();
    return this.rows.has(id);
  }

  async delete(id: ReplayStorageId): Promise<boolean> {
    this.assertOpen();
    return this.rows.delete(id);
  }

  async clear(): Promise<void> {
    this.assertOpen();
    this.rows.clear();
  }

  async getStats(): Promise<ReplayStorageStats> {
    this.assertOpen();
    let total = 0;
    for (const row of this.rows.values()) total += row.metadata.sizeBytes;
    return Object.freeze({
      count: this.rows.size,
      totalBytes: total,
      quotaBytes: null,
      usageBytes: total,
      backend: this.backend,
    });
  }

  async close(): Promise<void> {
    this.closed = true;
    this.rows.clear();
  }

  private assertOpen(): void {
    if (this.closed) {
      throw new ReplayStorageError('ReplayStorage: backend is closed');
    }
  }
}

