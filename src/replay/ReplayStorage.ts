/**
 * Backend-agnostic CRUD interface. Every backend implements every
 * method; callers code against this type so the replay menu can swap
 * IDB for localStorage at runtime without changes.
 *
 * Async on every method even when the backend can answer synchronously —
 * see the module header for the rationale.
 */
export interface ReplayStorage {
  /** Discriminator so HUDs / logs can show which backend is in use. */
  readonly backend: ReplayStorageBackend;

  /**
   * Persist a replay. Returns the storage-side metadata (including the
   * new row's id, `savedAt`, `sizeBytes`).
   *
   * Validation:
   *   • The replay must round-trip through {@link deserializeReplay} —
   *     a deformed `ReplayFile` is rejected before it touches storage.
   *   • If `options.id` is supplied, it must be a non-empty string.
   *   • If a row with that id already exists and `overwrite !== true`,
   *     throws {@link ReplayStorageError}.
   *
   * On quota-exceeded the partially-written row is cleaned up and the
   * backend rethrows {@link ReplayStorageQuotaExceededError}.
   */
  save(replay: ReplayFile, options?: SaveReplayOptions): Promise<StoredReplayMetadata>;

  /**
   * Load a replay by id. Returns `null` (not throws) when the row is
   * absent — callers that prefer the throwing form can use
   * {@link ReplayStorage.loadOrThrow}.
   *
   * The returned `replay` field is a frozen, validated {@link ReplayFile}.
   * If the stored bytes fail validation (an older app version wrote a
   * blob today's deserialiser refuses), throws
   * {@link ReplayStorageError}; the offending row stays in storage so
   * the user can decide to delete it manually.
   */
  load(id: ReplayStorageId): Promise<StoredReplay | null>;

  /** Sugar — `load()` that throws {@link ReplayNotFoundError} on miss. */
  loadOrThrow(id: ReplayStorageId): Promise<StoredReplay>;

  /**
   * List every replay's metadata, newest `savedAt` first. Cheap — does
   * not load any replay's input timeline.
   */
  list(): Promise<ReadonlyArray<StoredReplayMetadata>>;

  /** Membership check. */
  has(id: ReplayStorageId): Promise<boolean>;

  /**
   * Delete a row. Returns `true` when a row was removed, `false` when
   * the row was absent. Idempotent — never throws on a missing row.
   */
  delete(id: ReplayStorageId): Promise<boolean>;

  /** Drop every replay. */
  clear(): Promise<void>;

  /** Aggregate stats for the storage HUD. */
  getStats(): Promise<ReplayStorageStats>;

  /**
   * Release backend resources (close the IDB connection, drop the
   * localStorage prefix cache). Idempotent. After `close()` further
   * method calls throw {@link ReplayStorageError}.
   */
  close(): Promise<void>;
}

