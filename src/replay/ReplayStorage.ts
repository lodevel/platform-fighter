/**
 * Generic storage error. Distinct subclass so callers can:
 *
 *   try { await storage.save(replay); }
 *   catch (e) {
 *     if (e instanceof ReplayStorageQuotaExceededError) showFreeUpToast();
 *     else if (e instanceof ReplayStorageError)         showGenericError(e.message);
 *     else                                              throw e;
 *   }
 */
export class ReplayStorageError extends Error {
  constructor(message: string) {
    super(message);
    this.name = 'ReplayStorageError';
  }
}

/**
 * Thrown when a backend refuses a write because the browser quota was
 * hit. Distinct subclass so the replay menu can show a "free up space"
 * toast specifically for this condition. Wraps the original DOMException
 * for diagnostics.
 */
export class ReplayStorageQuotaExceededError extends ReplayStorageError {
  /** The native browser exception that triggered the wrap, if any. */
  public readonly cause?: unknown;
  constructor(message: string, cause?: unknown) {
    super(message);
    this.name = 'ReplayStorageQuotaExceededError';
    if (cause !== undefined) this.cause = cause;
  }
}

/**
 * Thrown when the caller asked for a row that doesn't exist on a path
 * that requires one (e.g. `delete(id)` returns `false` for a missing
 * row, but `save({ id, overwrite: true })` is conceptually an upsert
 * and never throws this).
 */
export class ReplayNotFoundError extends ReplayStorageError {
  constructor(id: ReplayStorageId) {
    super(`Replay not found: ${id}`);
    this.name = 'ReplayNotFoundError';
  }
}

/**
 * Thrown by every storage backend's `load()` (and the related
 * `loadOrThrow`) when the integrity check on a stored row fails — AC
 * 30104 Sub-AC 4.
 *
 * Wraps the underlying {@link ReplayIntegrityError} so the storage
 * layer's public surface still throws a single
 * {@link ReplayStorageError} family. The wrapped error is exposed via
 * `cause` (the underlying integrity error carries the
 * `expected` / `actual` / `algorithm` / `kind` diagnostic fields the
 * replay menu surfaces in its "Replay corrupted" toast).
 *
 * The replay menu can branch on this exact subclass to:
 *
 *   • Show a corruption-specific message (rather than the generic
 *     "couldn't load — JSON parse error").
 *   • Offer a "Delete corrupted replay" action that purges the row
 *     without trying to play it back.
 *   • Log a diagnostic event so we can spot patterns of corruption
 *     across users.
 */
export class ReplayCorruptedError extends ReplayStorageError {
  /** The underlying integrity error that triggered the wrap. */
  readonly cause: ReplayIntegrityError;
  /** Storage row id that failed the integrity check. */
  readonly id: ReplayStorageId;
  constructor(id: ReplayStorageId, cause: ReplayIntegrityError) {
    super(
      `Replay '${id}' failed integrity check (${cause.kind}): ${cause.message}`,
    );
    this.name = 'ReplayCorruptedError';
    this.cause = cause;
    this.id = id;
  }
}

