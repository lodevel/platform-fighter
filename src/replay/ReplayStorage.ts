/**
 * UTF-8 byte length of a string. `TextEncoder` is available in every
 * modern browser and Node 18+. Falls back to `s.length` (under-counts
 * multibyte) if absent — better than crashing on a save.
 */
export function utf8ByteLength(s: string): number {
  if (typeof TextEncoder !== 'undefined') {
    return new TextEncoder().encode(s).length;
  }
  return s.length;
}

/**
 * Serialise a replay to a stable JSON string for both IDB and
 * localStorage. Compact (no pretty-print) — this is machine-read
 * persistence, not a save-to-disk export.
 *
 * Re-uses {@link serializeReplayToString} so the same writer powers
 * `downloadReplay`, IDB persistence, and localStorage persistence —
 * three storage paths, one canonical wire format.
 */
export function serializeReplayForStorage(replay: ReplayFile): string {
  // The replay is already a frozen ReplayFile; unwrap the SerializeReplayOptions
  // shape the writer expects.
  const opts: SerializeReplayOptions = {
    matchConfig: replay.matchConfig,
    capturedFrames: replay.inputTimeline.entries.map((e) => ({
      frame: e.frame,
      inputs: e.inputs,
    })),
    recordedAt: new Date(replay.metadata.recordedAt),
    engineVersion: replay.metadata.engineVersion,
    notes: replay.metadata.notes,
    fixedTimestepMs: replay.metadata.fixedTimestepMs,
  };
  return serializeReplayToString(opts, false);
}

/**
 * Build {@link StoredReplayMetadata} from a replay + storage-side
 * fields. Centralised so both backends produce the same shape for the
 * same input.
 *
 * `checksum` is required so the metadata always carries a valid
 * integrity tag — passing `undefined` would let an upstream backend
 * forget to compute one and silently weaken the integrity guarantee.
 * The IDB / localStorage / memory backends compute the checksum from
 * the canonical serialised payload they're about to write and pass it
 * here.
 */
export function buildStoredReplayMetadata(
  id: ReplayStorageId,
  savedAt: string,
  sizeBytes: number,
  replay: ReplayFile,
  checksum: string,
  checksumAlgorithm: ReplayChecksumAlgorithm = CHECKSUM_ALGORITHM,
): StoredReplayMetadata {
  return Object.freeze({
    id,
    savedAt,
    sizeBytes,
    recordedAt: replay.metadata.recordedAt,
    durationFrames: replay.metadata.durationFrames,
    notes: replay.metadata.notes,
    stageId: replay.matchConfig.stageId,
    playerCount: replay.metadata.playerCount,
    rngSeed: replay.rngSeed,
    engineVersion: replay.metadata.engineVersion,
    checksum,
    checksumAlgorithm,
  });
}

/**
 * Compute the integrity checksum for a serialised replay payload —
 * AC 30104 Sub-AC 4. Convenience wrapper that lets every backend share
 * one entry-point so a future algorithm bump only has to change one
 * call site.
 */
export function computeReplayPayloadChecksum(serialized: string): string {
  return computeReplayChecksum(serialized);
}

/**
 * Verify a serialised replay payload against its stored checksum,
 * surfacing any mismatch as {@link ReplayCorruptedError} keyed by row
 * id — AC 30104 Sub-AC 4. Used by every backend's `load()` path so the
 * replay menu only has to catch one error type.
 */
export function assertReplayPayloadIntegrity(
  id: ReplayStorageId,
  serialized: string,
  expectedChecksum: string,
  algorithm: string = CHECKSUM_ALGORITHM,
): void {
  try {
    verifyReplayChecksum(serialized, expectedChecksum, algorithm);
  } catch (err) {
    if (err instanceof ReplayIntegrityError) {
      throw new ReplayCorruptedError(id, err);
    }
    throw err;
  }
}

/**
 * Validate a replay before write — round-trip it through the
 * deserialiser. Cheap (writes are rare relative to gameplay frames) and
 * catches the case where some upstream code produced a malformed
 * `ReplayFile`-ish object that would never load back.
 */
export function validateReplayForWrite(replay: ReplayFile): ReplayFile {
  if (replay === null || replay === undefined || typeof replay !== 'object') {
    throw new ReplayStorageError(
      'ReplayStorage.save: replay must be a ReplayFile object',
    );
  }
  // Quick magic / version sniff before the heavier round-trip.
  if ((replay as ReplayFile).format !== REPLAY_FORMAT_MAGIC) {
    throw new ReplayStorageError(
      `ReplayStorage.save: replay.format must equal '${REPLAY_FORMAT_MAGIC}'`,
    );
  }
  if ((replay as ReplayFile).version !== REPLAY_FORMAT_VERSION) {
    throw new ReplayStorageError(
      `ReplayStorage.save: replay.version must equal ${REPLAY_FORMAT_VERSION}`,
    );
  }
  // Round-trip via serialise → deserialise to confirm the bytes are valid.
  // We cannot just call `deserializeReplay(replay)` directly because the
  // input is already a ReplayFile (would round-trip-clone it for free,
  // but we want to fail fast on a malformed timeline).
  let parsed: ReplayFile;
  try {
    parsed = deserializeReplay(JSON.parse(serializeReplayForStorage(replay)));
  } catch (err) {
    if (err instanceof ReplayFileError) {
      throw new ReplayStorageError(
        `ReplayStorage.save: replay validation failed — ${err.message}`,
      );
    }
    throw err;
  }
  return parsed;
}

/** Default id factory — `crypto.randomUUID` when available, else fallback. */
export function defaultIdFactory(): () => ReplayStorageId {
  // Bind once. The crypto reference is checked lazily so a Node test
  // env without `crypto.randomUUID` (very old Node) still works.
  let counter = 0;
  return () => {
    const cryptoRef = (globalThis as { crypto?: Crypto }).crypto;
    if (cryptoRef !== undefined && typeof cryptoRef.randomUUID === 'function') {
      try {
        return cryptoRef.randomUUID();
      } catch {
        // Some sandboxes throw on `randomUUID` (insecure context, etc.) —
        // fall through to the counter fallback.
      }
    }
    counter += 1;
    return `replay-${Date.now().toString(36)}-${counter.toString(36)}`;
  };
}

/**
 * Detect whether a thrown value is a quota-exceeded `DOMException`. The
 * exact name varies between browsers (older Firefox uses
 * `NS_ERROR_DOM_QUOTA_REACHED`, Safari has historically thrown a plain
 * `Error`). This helper is conservative — it returns true on any
 * recognisable form.
 */
export function isQuotaExceededError(err: unknown): boolean {
  if (err === null || err === undefined) return false;
  if (typeof err !== 'object') return false;
  const e = err as { name?: unknown; code?: unknown; message?: unknown };
  if (typeof e.name === 'string') {
    if (
      e.name === 'QuotaExceededError' ||
      e.name === 'NS_ERROR_DOM_QUOTA_REACHED'
    ) {
      return true;
    }
  }
  if (typeof e.code === 'number' && (e.code === 22 || e.code === 1014)) {
    // Legacy DOMException codes for QUOTA_EXCEEDED_ERR (22) and
    // Firefox's old NS_ERROR_DOM_QUOTA_REACHED (1014).
    return true;
  }
  if (typeof e.message === 'string' && /quota/i.test(e.message)) {
    return true;
  }
  return false;
}

