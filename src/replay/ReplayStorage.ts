/**
 * Replay persistence layer — AC 30101 Sub-AC 1.
 *
 * What this module is
 * ===================
 *
 * The browser-side persistent home for finished replays. Where
 * {@link ./RecordingController} owns the live capture and
 * {@link ./downloadReplay} pushes a single file to the user's "Downloads"
 * folder, this module owns the **library** — the set of replays the
 * player accumulates over many sessions, browseable from the (later-AC)
 * replay menu and surfaceable as VCR fodder.
 *
 * Three backends, one interface
 * -----------------------------
 *
 *   1. {@link IndexedDBReplayStorage} (primary) — large quota,
 *      structured-clone payloads, async API. Default on Chrome desktop
 *      where M5 needs to live.
 *   2. {@link LocalStorageReplayStorage} (fallback) — tiny ~5 MB quota,
 *      synchronous, but universally available even in private-mode
 *      Firefox where IndexedDB sometimes refuses to open. Auto-selected
 *      when IDB is unusable.
 *   3. {@link MemoryReplayStorage} — non-persistent, in-process. Used by
 *      vitest, by headless CLI tools, and as the "first run, never
 *      saves" sandbox.
 *
 * `ReplayStorage` is the shared interface every backend implements; the
 * {@link openReplayStorage} factory probes the runtime and returns the
 * best backend it can stand up. Callers code against the interface — the
 * replay menu doesn't know whether the row it just deleted lived in IDB
 * or in `localStorage`.
 *
 * CRUD shape
 * ----------
 *
 *   • {@link ReplayStorage.save | save}(replay, options?)        → metadata (id, savedAt, sizeBytes, …)
 *   • {@link ReplayStorage.load | load}(id)                      → StoredReplay | null
 *   • {@link ReplayStorage.list | list}()                        → metadata[]   (newest first)
 *   • {@link ReplayStorage.has  | has}(id)                       → boolean
 *   • {@link ReplayStorage.delete | delete}(id)                  → boolean      (true if a row was removed)
 *   • {@link ReplayStorage.clear | clear}()                      → void         (drops every replay)
 *   • {@link ReplayStorage.getStats | getStats}()                → { count, totalBytes, quotaBytes? }
 *
 * Every method returns a `Promise` even when the backend (localStorage,
 * memory) could answer synchronously. The async surface lets the replay
 * menu code one path that works against IndexedDB *and* the fallback —
 * branching on backend type would leak persistence details into the UI.
 *
 * Quota handling
 * --------------
 *
 * Browsers cap storage with a {@link DOMException} of name
 * `QuotaExceededError` (or, on older Firefox, `NS_ERROR_DOM_QUOTA_REACHED`).
 * Both IDB and localStorage backends catch the native failure, free the
 * partially-written row if any, and rethrow as
 * {@link ReplayStorageQuotaExceededError}. The replay menu can `catch (e)
 * { if (e instanceof ReplayStorageQuotaExceededError) showFreeUpToast() }`
 * without sniffing browser-vendor strings.
 *
 * Determinism
 * -----------
 *
 * The persistence layer is **outside** the gameplay determinism boundary.
 * Replays it stores are themselves deterministic (the
 * {@link ReplayFile} schema encodes seed + input timeline), but the *id*
 * a save returns and the `savedAt` timestamp it stamps come from
 * caller-injectable factories. The default factories use `crypto.randomUUID`
 * (or a counter fallback) and `Date.now()` so production gets unique
 * sortable rows; tests inject deterministic stubs to assert exact bytes.
 *
 * Phaser-free
 * -----------
 *
 * No Phaser, Matter, or HTMLCanvas imports. This module touches only
 * `globalThis.indexedDB`, `globalThis.localStorage`, `crypto.randomUUID`,
 * and `Date.now` — all of which are stubbable via constructor options
 * for unit testing under plain Node (vitest's default environment).
 */

